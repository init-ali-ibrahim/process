class Product {
  final int id;
  final String name;
  final String slug;
  final int price;
  final int quantity;
  final String category;
  final String imageUrl;

  Product({
    required this.id,
    required this.name,
    required this.slug,
    required this.price,
    required this.quantity,
    required this.category,
    required this.imageUrl,
  });
}